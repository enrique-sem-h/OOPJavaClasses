package class2;

public class Calc {
    Double num1;
    Double num2;

    Double sum(Double num1, Double num2){
        return num1 + num2;
    }

    Double sub(Double num1, Double num2){
        return num1 - num2;
    }
    
    Double multiply(Double num1, Double num2){
        return num1 * num2;
    }
    
    Double divide(Double num1, Double num2){
        return num1 / num2;
    }
}
